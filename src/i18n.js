export function t(lang, key, vars) {
  const tbl = dict[lang] || dict.en;
  let s = tbl[key] ?? dict.en[key] ?? key;
  if (vars) {
    for (const k of Object.keys(vars)) s = s.replaceAll('{' + k + '}', vars[k]);
  }
  return s;
}

